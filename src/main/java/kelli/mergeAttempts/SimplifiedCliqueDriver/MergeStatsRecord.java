package kelli.mergeAttempts.SimplifiedCliqueDriver;

public class MergeStatsRecord {
	String numNetworks = "0";
	String numSubgroups = "0";
	String outfile = "";
	public String getNumNetworks() {
		return numNetworks;
	}
	public void setNumNetworks(String numNetworks) {
		this.numNetworks = numNetworks;
	}
	public String getNumSubgroups() {
		return numSubgroups;
	}
	public void setNumSubgroups(String numSubgroups) {
		this.numSubgroups = numSubgroups;
	}
	public String getOutfile() {
		return outfile;
	}
	public void setOutfile(String outfile) {
		this.outfile = outfile;
	}
	
}
